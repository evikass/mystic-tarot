/**
 * Психологический профиль по доминирующим арканам в раскладе.
 */
export function analyzePsychologicalProfile(cards: { card: TarotCard; isReversed: boolean }[]): {
  dominantElement: string
  elementCounts: Record<string, number>
  majorCount: number
  reversedCount: number
  insights: string[]
  affirmations: string[]
} {
  const elementCounts: Record<string, number> = {
    "Огонь": 0, "Вода": 0, "Воздух": 0, "Земля": 0, "Дух": 0,
  }
  let majorCount = 0
  let reversedCount = 0
  const insights: string[] = []
  const affirmations: string[] = []

  cards.forEach(({ card, isReversed }) => {
    if (card.suit === "major") {
      elementCounts["Дух"]++
      majorCount++
    } else {
      const el = card.element || "Дух"
      elementCounts[el] = (elementCounts[el] || 0) + 1
    }
    if (isReversed) reversedCount++
  })

  // Найти доминирующую стихию
  let dominantElement = "Дух"
  let max = 0
  Object.entries(elementCounts).forEach(([el, count]) => {
    if (count > max) {
      max = count
      dominantElement = el
    }
  })

  // Генерация инсайтов
  if (majorCount > cards.length / 2) {
    insights.push("Преобладание старших арканов указывает на важный жизненный этап, затронуты глубинные архетипические процессы. Это не повседневные вопросы — речь идёт о судьбоносных переменах.")
  } else if (majorCount === 0) {
    insights.push("Все карты — младшие арканы. Это повседневные вопросы, где ваша активность и решения играют ключевую роль. Судьба даёт пространство для вашего выбора.")
  }

  if (reversedCount > cards.length / 2) {
    insights.push("Большинство карт перевёрнуты — это указывает на внутреннее напряжение, нерешённые конфликты или торможение естественных процессов. Сейчас время для честного взгляда на то, что блокирует.")
  } else if (reversedCount === 0) {
    insights.push("Все карты прямые — энергия течёт свободно, есть ясность и согласие между сознательным и бессознательным. Используйте этот поток для действий.")
  }

  // Инсайты по доминирующей стихии
  const archetype = elementArchetypes[dominantElement]
  if (archetype && max > 0) {
    insights.push(`Доминирует стихия ${dominantElement} — активирован ${archetype.name}. ${archetype.coreWound}`)
    affirmations.push(archetype.affirmation)
  }

  // Дополнительные инсайты по распределению
  if (elementCounts["Огонь"] > 0 && elementCounts["Вода"] > 0) {
    insights.push("Огонь и Вода вместе — эмоционально насыщенный период. Страсть и чувствительность требуют баланса. Возможна алхимия или конфликт.")
  }
  if (elementCounts["Воздух"] > 0 && elementCounts["Земля"] > 0) {
    insights.push("Воздух и Земля вместе — время, когда идеи можно воплотить в практику. Хороший момент для конкретных шагов в проектах и материальных делах.")
  }
  if (elementCounts["Огонь"] >= 3) {
    insights.push("Сильное преобладание Огня — сейчас важно действовать, но не сгорать. Найдите фокус для пламени, иначе оно выгорит впустую.")
    affirmations.push("Я направляю свою энергию в выбранное русло и наслаждаюсь процессом.")
  }
  if (elementCounts["Вода"] >= 3) {
    insights.push("Сильное преобладание Воды — эмоции и интуиция сейчас ваши главные навигаторы. Доверяйте телу и снам, но не забывайте о берегах.")
    affirmations.push("Я доверяю своей интуиции и остаюсь в своей опоре.")
  }
  if (elementCounts["Воздух"] >= 3) {
    insights.push("Сильное преобладание Воздуха — время анализа, общения, обучения. Будьте осторожны с чрезмерным обдумыванием — иногда нужно просто действовать.")
    affirmations.push("Я выбираю ясное мышление и доверяю телу.")
  }
  if (elementCounts["Земля"] >= 3) {
    insights.push("Сильное преобладание Земли — период материального созидания и заземления. Хорошее время для дел, требующих терпения и мастерства.")
    affirmations.push("Я заземлён в изобилии жизни и доверяю процессу роста.")
  }

  return {
    dominantElement,
    elementCounts,
    majorCount,
    reversedCount,
    insights,
    affirmations,
  }
}

/**
 * Подсказки для карты дня по дате рождения.
 */
export function getDayCard(seed?: string): string {
  const today = new Date()
  const dateStr = today.toDateString()
  const hash = (dateStr + (seed || "")).split("").reduce((a, c) => a + c.charCodeAt(0), 0)
  return String(hash)
}
