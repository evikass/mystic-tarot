export function getNumerologyCompatibility(
  num1: number,
  num2: number
): Omit<NumerologyCompatibility, "lifePathPair" | "soulPair" | "personalityPair"> {
  const key1 = `${num1}-${num2}`
  const key2 = `${num2}-${num1}`
  return numberPairs[key1] || numberPairs[key2] || numberPairs["1-1"]
}

export function calculateNumerologyCompatibility(
  birth1: BirthDateResult,
  birth2: BirthDateResult
): NumerologyCompatibility {
  const base = getNumerologyCompatibility(birth1.lifePathNumber, birth2.lifePathNumber)
  return {
    ...base,
    lifePathPair: `${birth1.lifePathNumber}-${birth2.lifePathNumber}`,
    soulPair: `${birth1.soulNumber}-${birth2.soulNumber}`,
    personalityPair: `${birth1.personalityNumber}-${birth2.personalityNumber}`,
  }
}

