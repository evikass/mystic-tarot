function reduceToSingleDigit(n: number): number {
  // Не сокращаем мастер-числа 11, 22, 33
  if (n === 11 || n === 22 || n === 33) return n
  while (n > 9) {
    n = String(n).split("").reduce((sum, d) => sum + parseInt(d), 0)
    if (n === 11 || n === 22 || n === 33) return n
  }
  return n
}

export function calculateBirthDate(day: number, month: number, year: number): BirthDateResult | null {
  if (!day || !month || !year) return null
  if (day < 1 || day > 31 || month < 1 || month > 12 || year < 1900 || year > 2100) return null

  // Число Жизненного Пути — сумма всех цифр даты
  const allDigits = `${day}${month}${year}`.split("").map(Number).reduce((a, b) => a + b, 0)
  const lifePathNumber = reduceToSingleDigit(allDigits)

  // Число Души — день рождения (индийская система)
  const soulNumber = reduceToSingleDigit(day)

  // Число Личности — сумма цифр месяца
  const personalityNumber = reduceToSingleDigit(
    String(month).split("").map(Number).reduce((a, b) => a + b, 0)
  )

  return {
    lifePathNumber,
    soulNumber,
    personalityNumber,
    lifePath: numberMeanings[lifePathNumber] || numberMeanings[1],
    soul: numberMeanings[soulNumber] || numberMeanings[1],
    personality: numberMeanings[personalityNumber] || numberMeanings[1],
  }
}

