export function getZodiacSign(day: number, month: number): ZodiacSign | null {
  for (const sign of zodiacSigns) {
    const [startMonth, startDay, endMonth, endDay] = sign.dateRange
    if (startMonth === endMonth) {
      if (month === startMonth && day >= startDay && day <= endDay) return sign
    } else if (startMonth < endMonth) {
      if ((month === startMonth && day >= startDay) || (month === endMonth && day <= endDay)) return sign
    } else {
      // Козерог: декабрь-январь
      if ((month === startMonth && day >= startDay) || (month === endMonth && day <= endDay)) return sign
    }
  }
  return null
}

