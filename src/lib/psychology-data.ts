export function getChineseZodiac(year: number): ChineseZodiac {
  // Китайский новый год ~4 февраля, но для упрощения считаем по григорианскому году
  // Базовый год: 1984 = Крыса (Дерево) — начало 60-летнего цикла
  const baseYear = 1984
  const cycleYear = ((year - baseYear) % 60 + 60) % 60

  const animalIndex = cycleYear % 12
  const elementIndex = Math.floor(cycleYear / 12)

  const animal = chineseAnimals[animalIndex]
  const element = chineseElements[elementIndex]
  const elementColor = chineseElementColors[element]
  const animalData = animalPsychology[animal.animal]

  // Годы для этого животного (последние ~100 лет)
  const years: number[] = []
  for (let y = year - 60; y <= year + 60; y += 12) {
    if (y >= 1900 && y <= 2100) years.push(y)
  }

  return {
    animal: animal.animal,
    animalSymbol: animal.symbol,
    years,
    element,
    elementColor,
    archetype: animalData.archetype,
    psychology: animalData.psychology,
    strengths: animalData.strengths,
    challenges: animalData.challenges,
    career: animalData.career,
    relationships: animalData.relationships,
    compatibility: animalData.compatibility,
    affirmation: animalData.affirmation,
  }
}

