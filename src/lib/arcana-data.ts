/**
 * Карта-медитация дня (детерминированно по дате).
 */
export function getMeditationCard(date: Date = new Date()): {
  card: TarotCard
  affirmation: string
  meditation: string
  advice: string
} {
  const dateStr = date.toISOString().slice(0, 10)
  let hash = 0
  for (let i = 0; i < dateStr.length; i++) {
    hash = ((hash << 5) - hash) + dateStr.charCodeAt(i)
    hash |= 0
  }
  const seed = Math.abs(hash)
  const idx = seed % majorArcana.length
  const card = majorArcana[idx]
  const info = arcanaAffirmations[card.number] ?? arcanaAffirmations[0]
  return {
    card,
    affirmation: info.affirmation,
    meditation: info.meditation,
    advice: info.advice,
  }
}
