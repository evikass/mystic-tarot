/**
 * Персональные арканы Таро.
 *
 * 1. Аркан по дате рождения (Birth Card) — старший аркан, соответствующий
 *    полной дате рождения. Алгоритм: сложить все цифры даты, если сумма > 22 —
 *    вычесть 22. Получаем число 1-22 (22 = Шут).
 *
 * 2. Аркан года (Card of the Year) — аналогично, но вместо года рождения
 *    берётся текущий/выбранный год.
 *
 * 3. Карта-медитация дня с аффирмацией — детерминированно выбирается по дате.
 */

import { majorArcana, type TarotCard } from "./tarot-data"

// В majorArcana у нас 22 карты с number от 0 (Шут) до 21 (Мир).
// Для удобства расчёта арканов создадим индекс по number.
const majorByNumber: Map<number, TarotCard> = new Map()
for (const c of majorArcana) {
  majorByNumber.set(c.number, c)
}

/**
 * Рассчитать число аркана (1-22) из набора цифр.
 * Если сумма <= 22 — это и есть аркан.
 * Если > 22 — вычитаем 22 (не приводим к 1-9, как в классической нумерологии).
 * 22 соответствует Шуту (major-0 в нашей БД, но по формулам 22 = 0 = Шут).
 */
function calcArcanaNumber(digits: number[]): number {
  const sum = digits.reduce((a, b) => a + b, 0)
  if (sum <= 22) return sum === 0 ? 22 : sum  // 0 невозможен, но защита
  // Если сумма > 22, вычитаем 22
  let result = sum
  while (result > 22) result -= 22
  return result === 0 ? 22 : result
}

/**
 * Преобразовать число аркана (1-22) в карту.
 * По нашей системе: 1 = Маг (number=1), ..., 21 = Мир (number=21), 22 = Шут (number=0).
 */
function arcanaNumberToCard(num: number): TarotCard {
  // 22 → Шут (number=0)
  const cardNumber = num === 22 ? 0 : num
  return majorByNumber.get(cardNumber) ?? majorArcana[0]
}

/**
 * Аркан по дате рождения.
 * Алгоритм: сложить ВСЕ цифры даты (день.месяц.год), привести к 1-22.
 */
export function getBirthArcana(day: number, month: number, year: number): {
  card: TarotCard
  arcanaNumber: number
  rawSum: number
} {
  const dateStr = `${day}${month}${year}`
  const digits = dateStr.split("").map(d => parseInt(d))
  const rawSum = digits.reduce((a, b) => a + b, 0)
  const arcanaNumber = calcArcanaNumber(digits)
  return {
    card: arcanaNumberToCard(arcanaNumber),
    arcanaNumber,
    rawSum,
  }
}

/**
 * Аркан года (Card of the Year).
 * Алгоритм: сложить все цифры дня + месяца рождения + цифры выбранного года.
 * Например, для 15.03.1990 на 2026 год: 1+5+0+3+2+0+2+6 = 19 → 19 = Солнце.
 */
export function getYearArcana(day: number, month: number, year: number): {
  card: TarotCard
  arcanaNumber: number
  rawSum: number
} {
  const dateStr = `${day}${month}${year}`
  const digits = dateStr.split("").map(d => parseInt(d))
  const rawSum = digits.reduce((a, b) => a + b, 0)
  const arcanaNumber = calcArcanaNumber(digits)
  return {
    card: arcanaNumberToCard(arcanaNumber),
    arcanaNumber,
    rawSum,
  }
}

