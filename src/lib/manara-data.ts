/**
 * Получить все карты Манара (старшие + младшие).
 */
export function getAllManaraCards(): ManaraCard[] {
  return [...manaraCards, ...manaraMinorCards]
}

/**
 * Выбрать N случайных карт Манара без повторов.
 */
export function drawManaraCards(count: number): { card: ManaraCard; isReversed: boolean; position: string }[] {
  const all = getAllManaraCards()
  const positions = [
    "Вы",
    "Партнёр",
    "Что между вами",
    "Скрытое",
    "Совет",
    "Возможный итог",
    "Подсознательное",
    "Теневое",
  ]
  const shuffled = [...all].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count).map((card, i) => ({
    card,
    isReversed: Math.random() < 0.4,
    position: positions[i] ?? `Карта ${i + 1}`,
  }))
}
