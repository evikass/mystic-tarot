// Расчёт фазы Луны по дате (упрощённый алгоритм Коннона)
export function getMoonPhase(date: Date = new Date()): { phase: MoonPhase; phaseIndex: number; illumination: number; zodiacSign: string } {
  // Известное новолуние: 7 января 2000, 18:14 UTC
  const knownNewMoon = new Date(Date.UTC(2000, 0, 7, 18, 14))
  const synodicMonth = 29.530588853 // Средний синодический месяц

  const daysSinceNewMoon = (date.getTime() - knownNewMoon.getTime()) / (1000 * 60 * 60 * 24)
  const cyclePosition = ((daysSinceNewMoon % synodicMonth) + synodicMonth) % synodicMonth
  const phaseFraction = cyclePosition / synodicMonth

  // Определяем фазу
  let phaseIndex: number
  if (phaseFraction < 0.0625 || phaseFraction >= 0.9375) phaseIndex = 0 // Новолуние
  else if (phaseFraction < 0.1875) phaseIndex = 1 // Растущий серп
  else if (phaseFraction < 0.3125) phaseIndex = 2 // Первая четверть
  else if (phaseFraction < 0.4375) phaseIndex = 3 // Растущая Луна
  else if (phaseFraction < 0.5625) phaseIndex = 4 // Полнолуние
  else if (phaseFraction < 0.6875) phaseIndex = 5 // Убывающая Луна
  else if (phaseFraction < 0.8125) phaseIndex = 6 // Последняя четверть
  else phaseIndex = 7 // Убывающий серп

  // Освещённость (0-100%)
  const illumination = Math.round((1 - Math.cos(phaseFraction * 2 * Math.PI)) / 2 * 100)

  // Знак зодиака Луны (упрощённо)
  const moonZodiacDegree = (cyclePosition * 360 / synodicMonth) % 360
  const zodiacIndex = Math.floor(moonZodiacDegree / 30)
  const zodiacSigns = ["Овен ♈", "Телец ♉", "Близнецы ♊", "Рак ♋", "Лев ♌", "Дева ♍", "Весы ♎", "Скорпион ♏", "Стрелец ♐", "Козерог ♑", "Водолей ♒", "Рыбы ♓"]

  return {
    phase: moonPhases[phaseIndex],
    phaseIndex,
    illumination,
    zodiacSign: zodiacSigns[zodiacIndex] || "Овен ♈",
  }
}

// Благоприятные дни для разных дел по фазе Луны
export function getMoonAdvice(phaseIndex: number): { favorable: string[]; unfavorable: string[] } {
  switch (phaseIndex) {
    case 0: // Новолуние
      return {
        favorable: ["Новые проекты", "Медитация", "Планирование", "Постановка целей", "Очищение"],
        unfavorable: ["Хирургические операции", "Бракосочетание", "Крупные покупки"],
      }
    case 4: // Полнолуние
      return {
        favorable: ["Празднование", "Творчество", "Исцеление", "Зарядка воды", "Прощение"],
        unfavorable: ["Хирургические операции", "Стрижка волос", "Судебные дела", "Острые конфликты"],
      }
    case 1: case 2: case 3: // Растущая Луна
      return {
        favorable: ["Стрижка волос (для роста)", "Новые знакомства", "Инвестиции", "Обучение", "Старт бизнеса"],
        unfavorable: ["Удаление зубов", "Диеты для похудения"],
      }
    case 5: case 6: case 7: // Убывающая Луна
      return {
        favorable: ["Очищение организма", "Удаление волос", "Диеты", "Завершение дел", "Отпускание привязанностей"],
        unfavorable: ["Новые проекты", "Стрижка для роста", "Крупные инвестиции"],
      }
    default:
      return { favorable: [], unfavorable: [] }
  }
}
