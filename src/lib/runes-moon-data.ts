export interface RuneDraw {
  rune: Rune
  isReversed: boolean
  position: string
}

export function drawRunes(count: number = 3): RuneDraw[] {
  const positions = count === 3 ? ["Прошлое", "Настоящее", "Будущее"] : Array.from({ length: count }, (_, i) => `Руна ${i + 1}`)
  const shuffled = [...runes].sort(() => Math.random() - 0.5).slice(0, count)
  return shuffled.map((rune, i) => ({
    rune,
    isReversed: rune.id !== 25 && Math.random() < 0.35, // Пустая руна не переворачивается
    position: positions[i],
  }))
}

