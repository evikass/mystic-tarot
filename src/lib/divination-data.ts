// === БИОРИТМЫ ===
export interface BiorhythmResult {
  date: Date
  physical: number // -100 to 100
  emotional: number
  intellectual: number
  daysAlive: number
}

export function calculateBiorhythm(birthDate: Date, targetDate: Date = new Date()): BiorhythmResult {
  const msPerDay = 1000 * 60 * 60 * 24
  const daysAlive = Math.floor((targetDate.getTime() - birthDate.getTime()) / msPerDay)
  return {
    date: targetDate,
    physical: Math.round(Math.sin((2 * Math.PI * daysAlive) / 23) * 100),
    emotional: Math.round(Math.sin((2 * Math.PI * daysAlive) / 28) * 100),
    intellectual: Math.round(Math.sin((2 * Math.PI * daysAlive) / 33) * 100),
    daysAlive,
  }
}

export function getBiorhythmSeries(birthDate: Date, days: number = 30): BiorhythmResult[] {
  const series: BiorhythmResult[] = []
  const today = new Date()
  for (let i = -Math.floor(days / 2); i < Math.ceil(days / 2); i++) {
    const date = new Date(today)
    date.setDate(date.getDate() + i)
    series.push(calculateBiorhythm(birthDate, date))
  }
  return series
}

