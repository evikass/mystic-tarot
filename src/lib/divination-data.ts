// Генерация 6 линий из номера гексаграммы
function getHexagramLines(number: number): boolean[] {
  // Каждая гексаграмма имеет уникальную комбинацию из 6 линий
  // Нижняя триграмма (линии 1-3) и верхняя (линии 4-6)
  const binary = (number - 1).toString(2).padStart(6, "0")
  return binary.split("").map(b => b === "1")
}

export function getHexagram(number: number): Hexagram {
  const data = hexagramData[number] || { name: `Гексаграмма ${number}`, judgment: "", image: "", meaning: "", psychology: "" }
  const lines = getHexagramLines(number)
  return {
    number,
    name: data.name,
    lines,
    binary: lines.map(l => l ? "1" : "0").join(""),
    judgment: data.judgment,
    image: data.image,
    meaning: data.meaning,
    psychology: data.psychology,
  }
}

