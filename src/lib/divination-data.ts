function reduceToMaster(n: number): number {
  if (n === 11 || n === 22 || n === 33) return n
  while (n > 9) {
    n = String(n).split("").reduce((a, b) => a + parseInt(b), 0)
    if (n === 11 || n === 22) return n
  }
  return n
}

export function calculateNameNumerology(name: string): NameNumerology | null {
  if (!name || name.trim().length < 2) return null
  const upper = name.toUpperCase().replace(/[^А-ЯA-Z]/g, "")

  const vowels = upper.split("").filter(c => "АЕЁИОУЫЭЮЯAEIOUY".includes(c))
  const consonants = upper.split("").filter(c => !"АЕЁИОУЫЭЮЯAEIOUY".includes(c))

  const soulSum = vowels.reduce((sum, c) => sum + (letterValues[c] || 0), 0)
  const personalitySum = consonants.reduce((sum, c) => sum + (letterValues[c] || 0), 0)
  const destinySum = soulSum + personalitySum

  const soulNumber = reduceToMaster(soulSum) || 1
  const personalityNumber = reduceToMaster(personalitySum) || 1
  const destinyNumber = reduceToMaster(destinySum) || 1

  return {
    name,
    soulNumber,
    personalityNumber,
    destinyNumber,
    soulMeaning: numberMeanings[soulNumber] || numberMeanings[1],
    personalityMeaning: numberMeanings[personalityNumber] || numberMeanings[1],
    destinyMeaning: numberMeanings[destinyNumber] || numberMeanings[1],
  }
}

// === ШЕРИНГ РЕЗУЛЬТАТА (Web Share API) ===
export async function shareResult(title: string, text: string): Promise<boolean> {
  if (typeof navigator !== "undefined" && navigator.share) {
    try {
      await navigator.share({ title, text })
      return true
    } catch {
      return false
    }
  }
  // Фолбэк — копирование в буфер обмена
  if (typeof navigator !== "undefined" && navigator.clipboard) {
    try {
      await navigator.clipboard.writeText(`${title}\n\n${text}`)
      return true
    } catch {
      return false
    }
  }
  return false
}
