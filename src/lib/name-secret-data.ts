// ===== Index for fast lookup =====
const nameIndex: Map<string, NameSecret> = new Map()
for (const n of nameSecrets) {
  nameIndex.set(n.name.toLowerCase(), n)
  // Also index by short forms
  for (const sf of n.shortForms) {
    if (!nameIndex.has(sf.toLowerCase())) {
      nameIndex.set(sf.toLowerCase(), n)
    }
  }
}

/**
 * Найти тайну имени по строке (имя или уменьшительная форма).
 * Возвращает null, если имя не найдено в базе.
 */
export function findNameSecret(name: string): NameSecret | null {
  if (!name) return null
  const normalized = name.trim().toLowerCase()
  if (!normalized) return null
  return nameIndex.get(normalized) ?? null
}

/**
 * Получить список всех имён определённого пола (для подсказок).
 */
export function getNamesByGender(gender: "male" | "female"): NameSecret[] {
  return nameSecrets.filter(n => n.gender === gender)
}

/**
 * Получить случайное «имя дня».
 */
export function getNameOfTheDay(): NameSecret {
  const today = new Date()
  const seed = today.getDate() + today.getMonth() * 31
  return nameSecrets[seed % nameSecrets.length]
}

/**
 * Поиск имён с подсказкой (для автодополнения).
 */
export function searchNames(query: string, limit = 8): NameSecret[] {
  if (!query.trim()) return []
  const q = query.trim().toLowerCase()
  const results: NameSecret[] = []
  for (const n of nameSecrets) {
    if (n.name.toLowerCase().includes(q) || n.shortForms.some(sf => sf.toLowerCase().includes(q))) {
      results.push(n)
      if (results.length >= limit) break
    }
  }
  return results
}
