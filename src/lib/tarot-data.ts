export const allTarotCards: TarotCard[] = [
  ...majorArcana,
  ...wands,
  ...cups,
  ...swords,
  ...pentacles,
]

export function getCardById(id: string): TarotCard | undefined {
  return allTarotCards.find((c) => c.id === id)
}

export function shuffleCards(cards: TarotCard[] = allTarotCards): TarotCard[] {
  const result = [...cards]
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[result[i], result[j]] = [result[j], result[i]]
  }
  return result
}

export function drawCards(count: number, reversed: boolean = true): { card: TarotCard; isReversed: boolean }[] {
  const shuffled = shuffleCards()
  return shuffled.slice(0, count).map((card) => ({
    card,
    isReversed: reversed ? Math.random() < 0.35 : false,
  }))
}

